CREATE TABLE PIZZA(
    ID_PIZZA INT AUTO_INCREMENT PRIMARY KEY,
    ID_TIPOCARNE INT,
    ID_TIPOVEGETALES INT,
    ID_TIPO_MASA INT,
    ID_TAMANHO INT,
    PRECIO_FINAL DECIMAL(20,2),
    FECHA DATETIME,
    DESCRIPCION VARCHAR(500),
    ID_CLIENTES INT,
    FOREIGN KEY (ID_TAMANHO) REFERENCES TAMANHO_PIZZA(ID_TAMANHO),
    FOREIGN KEY (ID_TIPOCARNE) REFERENCES TIPO_CARNES(ID_TIPOCARNE),
    FOREIGN KEY (ID_TIPOVEGETALES) REFERENCES TIPOS_VEGETALES(ID_TIPOVEGETALES),
    FOREIGN KEY (ID_TIPO_MASA) REFERENCES TIPO_MASAS(ID_TIPO_MASA),
    FOREIGN KEY (ID_CLIENTES) REFERENCES CLIENTES(ID_CLIENTES)
);

