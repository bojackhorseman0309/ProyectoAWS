USE Pizzeria;

CREATE TABLE CLIENTES(
    ID_CLIENTES INT AUTO_INCREMENT PRIMARY KEY,
    NOMBRE VARCHAR(100),
    APELLIDO VARCHAR(100),
    SEGUNDO_APELLIDO  VARCHAR(100),
    DIRECCION VARCHAR(300),
    CLAVE VARCHAR(255),
    TELEFONO INT
);

